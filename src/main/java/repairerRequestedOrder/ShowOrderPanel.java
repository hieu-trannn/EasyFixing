    public void UpdateShowOrderPanel(Vector data, int idOrder) {
        labelCustomerName.setText((String) data.get(0));
        labelServiceName.setText((String) data.get(1));
        labelDescription.setText((String) data.get(2));
        labelPrice.setText((String) data.get(3));
        String status;
        switch ((int) data.get(4)) {
            case 0:
                status = "Haven't received";
                break;
            case 1:
                status = "Rejected";
                break;
            case 2:
                status = "Accepted";
                break;
            case 3:
                status = "Haven't paid";
                break;
            case 4:
                status = "Repairing";
                break;
            case 5:
                status = "Done";
                break;
            default:
                throw new AssertionError();
        }
        labelStatus.setText(status);
        labelTime.setText((String) data.get(5));
        setIdOrder(idOrder);
    }

